def main(argv=None):
    '''this is called if run from command line'''
    (prog, args) = interpretCmdLine()
    parser = argparse.ArgumentParser(prog, description='WorkingNamePatternScanner')
    # parser.add_argument()
    args = parser.parse_args(args)
    
    lineregex = re.compile(r"""(^.+)\t(.*)""")
    rawText = ""
    for line in sys.stdin:
        # print line
        m = lineregex.match(line) 
        if m:
            url = m.group(1)
            rawText = m.group(2)
            post = json.loads(rawText)
            if isinstance(post, dict):
                bodyText = post.get('bodyText')
                if bodyText and bodyText.get('tokens'):
                    nameScanner = PatternScanner(bodyText['tokens'], 'names')
                    # arguably we should only add novel matches
                    # implemention would be to represent/convert 
                    # workingNameMatch dicts to namedtuples
                    # use a set to uniquify
                    # then convert back to dict to write out as JSON
                    # for now, we will simply append to list
                    bodyWorkingNameMatches = bodyText.get('workingNameMatches') or []
                    for (phrase, subseqs) in nameScanner.scan():
                        for subseq in subseqs:
                            # In our 13.M.N patterns, the last matched word is supposed to be the name
                            wname = subseq[-1]
                            if wname.isalpha() and len(wname)>=2:
                                # right now our 14.M.N non-name patterns anticipate a single 
                                # word, but it could be a phrase in general
                                # so go ahead and use the general mechanism
                                nonnameScanner = PatternScanner([wname], 'nonname')
                                if not nonnameScanner.scan():
                                    resultJson = {"objectType": "workingNameMatch",
                                                  "phrasePattern":
                                                      {"indic": phrase.indic,
                                                       "category": phrase.category,
                                                       "family": phrase.family,
                                                       # the pattern is just too big for 13.5.1 and 13.5.2
                                                       "tokenRegexPattern": None if (phrase.indic[0]==13 and phrase.indic[1]==5) else str(phrase.pattern),
                                                       "weight": phrase.weight,
                                                       "workingName": wname},
                                                  "tokenSequence": subseq}
                                    bodyWorkingNameMatches.append(resultJson)
                    # is it good practice to record empty results
                    # or should we just not have any such entry
                    bodyText['workingNameMatches'] = bodyWorkingNameMatches

            js = json.dumps(post, sort_keys=True, indent=None)
            print >> sys.stdout, "%s\t%s" % (url, js)
    print >> sys.stdout, ""

# call main() if this is run as standalone
if __name__ == "__main__":
    sys.exit(main())
